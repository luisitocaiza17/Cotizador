package com.qbe.cotizador.servlets.producto.agricola;

public class AgriCrucesEstadoSucre {

	private String idCotizacion;
	private String tramite;
	private String estadoQbe;
	private String estadoSucre;
	private String observacion;
	
	
	
	
	public String getObservacion() {
		return observacion;
	}
	public void setObservacion(String observacion) {
		this.observacion = observacion;
	}
	public String getIdCotizacion() {
		return idCotizacion;
	}
	public void setIdCotizacion(String idCotizacion) {
		this.idCotizacion = idCotizacion;
	}
	public String getTramite() {
		return tramite;
	}
	public void setTramite(String tramite) {
		this.tramite = tramite;
	}
	public String getEstadoQbe() {
		return estadoQbe;
	}
	public void setEstadoQbe(String estadoQbe) {
		this.estadoQbe = estadoQbe;
	}
	public String getEstadoSucre() {
		return estadoSucre;
	}
	public void setEstadoSucre(String estadoSucre) {
		this.estadoSucre = estadoSucre;
	}
	
	
		
}
